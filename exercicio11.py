
primeiro_inteiro = int(input('Informe o primeiro número: '))
segundo_inteiro = int(input('Informe o segundo número: '))
terceiro_real = float(input('Informe um número real:  '))

a = primeiro_inteiro * 2 + segundo_inteiro / 2
b = primeiro_inteiro * 3 + terceiro_real
c = terceiro_real ** 3 

print(f'O produto do dobro do primeiro com metade do segundo, {a} ')
print(f'A soma do triplo do primeiro com o terceiro, {b}')
print(f'O terceiro elevado ao cubo, {c:.2f}')



